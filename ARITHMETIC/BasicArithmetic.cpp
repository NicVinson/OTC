#include <iostream>

int main(){
    //variables for calculations
    int a = 100;
    int b = 3;
    //output
    std::cout << a << " + " << b << " = " << a + b << "\n";
    std::cout << a << " - " << b << " = " << a - b << "\n";
    std::cout << a << " / " << b << " = " << a / b << "\n";
    std::cout << a << " * " << b << " = " << a * b << "\n";
    std::cout << a << " % " << b << " = " << a % b;

    return 0;
}